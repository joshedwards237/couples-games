// Build the bundled 5-letter dictionary consumed by isValidGuess().
//
// Source: an-array-of-english-words (devDependency). Filtered to
// lowercase alphabetic 5-letter strings, merged with the app's
// word_pool seed (which is the ground truth for puzzle answers), then
// emitted as src/lib/dictionary.generated.ts.
//
// Run via: npm run build:dict
// Re-run whenever the word_pool or dict source changes.

import fs from 'node:fs/promises';
import path from 'node:path';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';

// Newer Node requires import attributes for JSON imports; the npm
// package ships a .json file, so use CommonJS require to stay portable.
const require = createRequire(import.meta.url);
const englishWords = require('an-array-of-english-words');

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const schemaPath = path.join(repoRoot, 'supabase/schema.sql');
const outPath = path.join(repoRoot, 'src/lib/dictionary.generated.ts');

// --- 1) English dictionary, 5 letters, alphabetic only, uppercased.
const fiveLetter = englishWords
  .filter((w) => /^[a-z]{5}$/.test(w))
  .map((w) => w.toUpperCase());

// --- 2) Parse word_pool seed values from schema.sql so every puzzle
// answer is guaranteed to validate, even the obscure ones.
const schemaSrc = await fs.readFile(schemaPath, 'utf8');
const poolMatches = schemaSrc.match(/'[A-Z]{5}'/g) ?? [];
const pool = poolMatches.map((s) => s.slice(1, -1));

// --- 3) Merge + dedupe + sort.
const set = new Set([...fiveLetter, ...pool]);
const all = Array.from(set).sort();

// Sanity guard — if the build produced a suspiciously small set, bail
// so a broken run doesn't ship a half-empty dict.
if (all.length < 10_000) {
  console.error(`Dictionary too small (${all.length}); aborting.`);
  process.exit(1);
}

// --- 4) Emit TS. Wrap Set construction behind a lazy getter so cold
// boot doesn't block on hydrating the Set when it's only needed once
// the user submits a guess.
const lines = [];
lines.push('// Auto-generated by scripts/build-dictionary.mjs. Do not edit by hand.');
lines.push('// Re-run `npm run build:dict` to refresh from the English word list');
lines.push('// and the current word_pool seed values in supabase/schema.sql.');
lines.push('');
lines.push(`// ${all.length} five-letter words.`);
lines.push('const WORDS = [');
// Chunk 10 per line, each word uppercased and quoted, for file-size and
// human-diff sanity when the list churns.
for (let i = 0; i < all.length; i += 10) {
  const chunk = all.slice(i, i + 10).map((w) => `'${w}'`).join(',');
  lines.push(`  ${chunk}${i + 10 < all.length ? ',' : ''}`);
}
lines.push('] as const;');
lines.push('');
lines.push('let cached: Set<string> | null = null;');
lines.push('export function getValidGuesses(): Set<string> {');
lines.push('  if (!cached) cached = new Set<string>(WORDS);');
lines.push('  return cached;');
lines.push('}');
lines.push('');

await fs.writeFile(outPath, lines.join('\n'), 'utf8');

console.log(`Wrote ${all.length.toLocaleString()} words to ${path.relative(repoRoot, outPath)}`);
console.log(`  from english-words: ${fiveLetter.length.toLocaleString()}`);
console.log(`  from word_pool:     ${pool.length.toLocaleString()}`);
console.log(`  total after dedupe: ${all.length.toLocaleString()}`);
